    {
        $this->validate($request, [
            'npm' => 'required',
            'password' => 'required',
        ]);
        $cek = Auth::guard('mahasiswa')->attempt(['npm_mahasiswa' => $request->npm, 'password' => $request->password]);
        if ($cek) {
            if (auth()->guard('mahasiswa')->user()->role_id == 1) {
                dd('aslab');
            } else if (auth()->guard('mahasiswa')->user()->role_id == 2) {
